package com.neusoft.dms.prp.domain;

public class Prp {
	private int prpId;
	private String prpAbbr;
	private String prpName;
	private String remark;

	public int getPrpId() {
		return prpId;
	}
	public void setPrpId(int prpId) {
		this.prpId = prpId;
	}
	public String getPrpAbbr() {
		return prpAbbr;
	}
	public void setPrpAbbr(String prpAbbr) {
		this.prpAbbr = prpAbbr;
	}
	public String getPrpName() {
		return prpName;
	}
	public void setPrpName(String prpName) {
		this.prpName = prpName;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
