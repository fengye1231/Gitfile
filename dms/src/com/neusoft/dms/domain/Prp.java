package com.neusoft.dms.domain;

public class Prp {
	private int prpID;
	private String prpAbbr;
	private String prpName;
	private String remark;
	public int getPrpID() {
		return prpID;
	}
	public void setPrpID(int prpID) {
		this.prpID = prpID;
	}
	public String getPrpAbbr() {
		return prpAbbr;
	}
	public void setPrpAbbr(String prpAbbr) {
		this.prpAbbr = prpAbbr;
	}
	public String getPrpName() {
		return prpName;
	}
	public void setPrpName(String prpName) {
		this.prpName = prpName;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
