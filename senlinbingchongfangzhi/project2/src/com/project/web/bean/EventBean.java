package com.project.web.bean;

import java.sql.Date;
import java.util.*;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;
/**
 * 事件实体类
 * @author TE
 *
 */
@JsonIgnoreProperties(value = {"handler"})
public class EventBean {
	/**
	 * 事件主键
	 */
   private int id;
   /**
    * 事件名称
    */
   private String name;
   /**
    * 灾区图片
    */
   private String imgPath;
   /**
    * 事件时间
    */
   private String date;
   /**
    * 灾害类型
    */
   private String type;
   /**
    * 灾情阶段
    */
   private String phase;
   /**
    * 灾情描述
    */
   private String describe;
   /**
    * 发现途径
    */
   private String findPath;
   /**
    * 区域对象
    */
   private String area;
   /**
    * 初步损失
    */
   private String lose;
   /**
    * 影响面积
    */
   private String affectArea;
   /**
    * 防治方案
    */
   private String plan;
   /**
    * 专家意见
    */
   private String opinon;
   /**
    * 会商集合
    */
   private List<ConsultationBean> list;


	
	public EventBean(int id, String name, String imgPath, String date, String type, String phase, String describe,
		String findPath, String area, String lose, String affectArea, String plan, String opinon) {
	super();
	this.id = id;
	this.name = name;
	this.imgPath = imgPath;
	this.date = date;
	this.type = type;
	this.phase = phase;
	this.describe = describe;
	this.findPath = findPath;
	this.area = area;
	this.lose = lose;
	this.affectArea = affectArea;
	this.plan = plan;
	this.opinon = opinon;
}

	public EventBean() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImgPath() {
		return imgPath;
	}

	public void setImgPath(String imgPath) {
		this.imgPath = imgPath;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getPhase() {
		return phase;
	}

	public void setPhase(String phase) {
		this.phase = phase;
	}

	public String getDescribe() {
		return describe;
	}

	public void setDescribe(String describe) {
		this.describe = describe;
	}

	public String getFindPath() {
		return findPath;
	}

	public void setFindPath(String findPath) {
		this.findPath = findPath;
	}


	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getLose() {
		return lose;
	}

	public void setLose(String lose) {
		this.lose = lose;
	}

	public String getAffectArea() {
		return affectArea;
	}

	public void setAffectArea(String affectArea) {
		this.affectArea = affectArea;
	}

	public String getPlan() {
		return plan;
	}

	public void setPlan(String plan) {
		this.plan = plan;
	}

	public String getOpinon() {
		return opinon;
	}

	public void setOpinon(String opinon) {
		this.opinon = opinon;
	}

	public List<ConsultationBean> getList() {
		return list;
	}

	public void setList(List<ConsultationBean> list) {
		this.list = list;
	}

	@Override
	public String toString() {
		return "EventBean [id=" + id + ", name=" + name + ", imgPath=" + imgPath + ", date=" + date + ", type=" + type
				+ ", phase=" + phase + ", describe=" + describe + ", findPath=" + findPath + ", area=" + area
				+ ", lose=" + lose + ", affectArea=" + affectArea + ", plan=" + plan + ", opinon=" + opinon + ", list="
				+ list + "]";
	}


   
   
	
   
   

}