package com.project.web.bean;

/**
 * 病害实体类
 * 
 * @author TE
 *
 */
public class DiseaseBean {
	/**
	 * 病害id
	 */
	private int id;
	/**
	 * 病害名称
	 */
	private String name;
	/**
	 * 病害原因
	 */
	private String pathogen;
	/**
	 * 发病规律
	 */
	private String law;
	/**
	 * 病害图片
	 */
	private String imgPath;
	/**
	 * 防治措施
	 */
	private String prevention;
	/**
	 * 发病症状
	 */
	private String sysmptom;
	/**
	 * 主要危害
	 */
	private String harm;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPathogen() {
		return pathogen;
	}

	public void setPathogen(String pathogen) {
		this.pathogen = pathogen;
	}

	public String getLaw() {
		return law;
	}

	public void setLaw(String law) {
		this.law = law;
	}

	public String getImgPath() {
		return imgPath;
	}

	public void setImgPath(String imgPath) {
		this.imgPath = imgPath;
	}

	public String getPrevention() {
		return prevention;
	}

	public void setPrevention(String prevention) {
		this.prevention = prevention;
	}

	public String getSysmptom() {
		return sysmptom;
	}

	public void setSysmptom(String sysmptom) {
		this.sysmptom = sysmptom;
	}

	public String getHarm() {
		return harm;
	}

	public void setHarm(String harm) {
		this.harm = harm;
	}

	public DiseaseBean() {
		super();
		// TODO Auto-generated constructor stub
	}

	@Override
	public String toString() {
		return "DiseaseBean [id=" + id + ", name=" + name + ", pathogen=" + pathogen + ", law=" + law + ", imgPath="
				+ imgPath + ", prevention=" + prevention + ", sysmptom=" + sysmptom + ", harm=" + harm + "]";
	}

	public DiseaseBean(String name, String pathogen, String law, String imgPath, String prevention, String sysmptom,
			String harm) {
		super();
		this.name = name;
		this.pathogen = pathogen;
		this.law = law;
		this.imgPath = imgPath;
		this.prevention = prevention;
		this.sysmptom = sysmptom;
		this.harm = harm;
	}

}